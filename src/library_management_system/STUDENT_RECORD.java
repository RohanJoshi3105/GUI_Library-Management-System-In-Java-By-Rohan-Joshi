    private void SEARCHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SEARCHActionPerformed
        // TODO add your handling code here:
        long p=Long.parseLong(prn.getText());
        try{
           
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/smartlib","root","root");
            Statement stmt=con.createStatement();
      
           // String query3="insert into return_book(book_id,book_name,author,category) values('"+bid.getText()+"','"+bname.getText()+"','"+author.getText()+"','"+category.getText()+"');";
            String query1="select*from student where registration_no="+p+";";
   
            ResultSet rs=stmt.executeQuery(query1);
            String NAME,BRANCH;
            long PRN,MOB;
            while(rs.next()){
                PRN=rs.getLong("registration_no");
                NAME=rs.getString("name");
                MOB=rs.getLong("mobile_no");
                BRANCH=rs.getString("branch");
                    
            /*  {
                NO=rs.getString("issued");
                no.setSelected(true);
              }
                */

                prn.setText(Long.toString(PRN));
                name.setText(NAME);
                mob.setText(Long.toString(MOB));
                branch.setText(BRANCH);
            }

        }
        catch(Exception err){
            System.out.println(err.getMessage());
            JOptionPane.showMessageDialog(null,"error");
            
        }

    }//GEN-LAST:event_SEARCHActionPerformed

